//for loops

for(let i = 1; i <= 10; i++){
    console.log(i);
} // 1 to 10

console.log("");

for(let i = 10; i >= 0; i--){
    console.log(i);
} // 10 to 1

console.log("");


//while loops
let increment = 0;
while(increment < 10){
    increment++;
    console.log(increment);
} // 1 to 10

console.log("");

let decrement = 11;
while(decrement > 0){
    decrement--;
    console.log(decrement);
} // 10 to 0

